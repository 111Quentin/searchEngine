  private static X509TrustManager getTrustManager() {
    // 实现一个X509TrustManager接口，用于绕过验证，不用修改里面的方法
    X509TrustManager trustManager =
        new X509TrustManager() {
          @Override
          public void checkClientTrusted(
              java.security.cert.X509Certificate[] paramArrayOfX509Certificate,
              String paramString) {}

          @Override
          public void checkServerTrusted(
              java.security.cert.X509Certificate[] paramArrayOfX509Certificate,
              String paramString) {}

          @Override
          public java.security.cert.X509Certificate[] getAcceptedIssuers() {
            return null;
          }
        };

    return trustManager;
  }

  /**
   * 绕过验证
   *
   * @return
   * @throws NoSuchAlgorithmException
   * @throws KeyManagementException
   */
  private static SSLContext createIgnoreVerifySSL() {

    X509TrustManager trustManager = getTrustManager();

    SSLContext sc = null;
    try {
      sc = SSLContext.getInstance("SSLv3");
      sc.init(null, new TrustManager[] {trustManager}, null);
    } catch (NoSuchAlgorithmException e) {
      e.printStackTrace();
      logger.error("https create ignore ssl NoSuchAlgorithmException", e);
    } catch (KeyManagementException e) {
      e.printStackTrace();
      logger.error("https create ignore ssl KeyManagementException", e);
    }
    return sc;
  }

  static {
    try {

      System.out.println("初始化HttpClientTest~~~开始");
      SSLContextBuilder builder = new SSLContextBuilder();
      builder.loadTrustMaterial(null, new TrustSelfSignedStrategy());
      SSLConnectionSocketFactory sslsf = new SSLConnectionSocketFactory(builder.build());
      // 配置同时支持 HTTP 和 HTPPS
      Registry<ConnectionSocketFactory> socketFactoryRegistry2 =
          RegistryBuilder.<ConnectionSocketFactory>create()
              .register("http", PlainConnectionSocketFactory.getSocketFactory())
              .register("https", sslsf)
              .build();

      SSLContext sslcontext = createIgnoreVerifySSL();

      // 设置协议http和https对应的处理socket链接工厂的对象
      Registry<ConnectionSocketFactory> socketFactoryRegistry =
          RegistryBuilder.<ConnectionSocketFactory>create()
              .register("http", PlainConnectionSocketFactory.INSTANCE)
              .register("https", new SSLConnectionSocketFactory(sslcontext))
              .build();
      PoolingHttpClientConnectionManager connManager =
          new PoolingHttpClientConnectionManager(socketFactoryRegistry);

      // 初始化连接管理器
      poolConnManager = new PoolingHttpClientConnectionManager(socketFactoryRegistry);
      // 将最大连接数增加到200，实际项目最好从配置文件中读取这个值
      poolConnManager.setMaxTotal(200);
      // 设置最大路由
      poolConnManager.setDefaultMaxPerRoute(2);
      // 根据默认超时限制初始化requestConfig
      int socketTimeout = 20000;
      int connectTimeout = 10000;
      int connectionRequestTimeout = 20000;
      requestConfig =
          RequestConfig.custom()
              .setConnectionRequestTimeout(connectionRequestTimeout)
              .setSocketTimeout(socketTimeout)
              .setConnectTimeout(connectTimeout)
              .build();

      // 初始化httpClient
      httpClient = getConnection();

    } catch (NoSuchAlgorithmException e) {
      e.printStackTrace();
    } catch (KeyStoreException e) {
      e.printStackTrace();
    } catch (KeyManagementException e) {
      e.printStackTrace();
    }
  }

  public static CloseableHttpClient getConnection() {
    CloseableHttpClient httpClient =
        HttpClients.custom()
            // 设置连接池管理
            .setConnectionManager(poolConnManager)
            // 设置请求配置
            .setDefaultRequestConfig(requestConfig)
            // 设置重试次数
            .setRetryHandler(new DefaultHttpRequestRetryHandler(3, true))
            .build();

    if (poolConnManager != null && poolConnManager.getTotalStats() != null) {
      System.out.println("now client pool " + poolConnManager.getTotalStats().toString());
    }

    return httpClient;
  }

  public static void httpGet(String url) {
    HttpGet httpGet = new HttpGet(url);
    CloseableHttpResponse response = null;
    try {
      response = httpClient.execute(httpGet);
      HttpEntity entity = response.getEntity();
      String result = EntityUtils.toString(entity, StandardCharsets.UTF_8);
      EntityUtils.consume(entity);
      System.out.println(url + ":" + result.length());
    } catch (IOException e) {
      e.printStackTrace();
    } finally {
      try {
        if (response != null) response.close();
      } catch (IOException e) {
        e.printStackTrace();
      }
    }
  }

